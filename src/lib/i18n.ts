export function resolveAppLocale(
  preference: LanguagePreference,
  systemLanguages: readonly string[] | string | null | undefined
): AppLocale {
  if (preference !== "system") return preference;

  const languages = typeof systemLanguages === "string" ? [systemLanguages] : systemLanguages ?? [];
  const primary = languages.find((language) => language.trim())?.toLowerCase() ?? "en";
  return primary === "zh" || primary.startsWith("zh-") ? "zh-CN" : "en";
}

export function browserLanguages(): readonly string[] {
  if (typeof navigator === "undefined") return ["en"];
  return navigator.languages?.length ? navigator.languages : [navigator.language || "en"];
}

export function translate(locale: AppLocale, message: string, values: TranslationValues = {}): string {
  const template = locale === "zh-CN" ? zhCN[message] ?? message : message;
  return interpolate(template, values);
}

export function createTranslator(locale: AppLocale): Translator {
  return (message, values) => translate(locale, message, values);
}

